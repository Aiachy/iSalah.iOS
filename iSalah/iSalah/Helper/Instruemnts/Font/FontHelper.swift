import SwiftUI

struct FontHelper {
    
    enum Dubai: String {
        case regular = "Dubai-Regular.ttf"
        case medium = "Dubai-Medium.ttf"
        case light = "Dubai-Light.ttf"
        case bold = "Dubai-Bold.ttf"
    }
    
    enum Size: CGFloat {
        case xxs = 10, xs = 12, s = 14, m = 16, l = 18, xl = 20, xxl = 24, xxxl = 28
        case h1 = 32, h2 = 36, h3 = 42, h4 = 48
    }
    
    
}
